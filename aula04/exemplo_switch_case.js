let dia = "quarta-feira";
switch (dia) {
  case "segunda-feira":
    alert("Início da semana!");
    break;
  case "quarta-feira":
    alert("Meio da semana!");
    break;
  case "sexta-feira":
    alert("Fim de semana chegando!");
    break;
  default:
    alert("Dia comum.");
}
// Saída: Meio da semana!

